    public function list(){
        $data = City::where('deleted_at', NULL)->where('active', 1)->where('id', '>', 1)->orderBy('state', 'ASC')->orderBy('name', 'ASC')->get();

        // Returns json for the API requests
        if(request('request') == 'api'){
            return response()->json(['cities' => $data]);
        }
    }

